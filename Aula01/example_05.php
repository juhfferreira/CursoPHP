<?php

	$expression=false;

	//O WHILE verifica a condicao e depois executa ou nao o comando
	while($expression){
		echo "[WHILE]Oi, eu sou Goku!!\n";
	}
	//O DO WHILE executa o comando pelo menos uma vez antes de verificar a expressao
	do {
		echo "[DO WHILE]Oi, eu sou Goku!!\n";
	} while($expression);